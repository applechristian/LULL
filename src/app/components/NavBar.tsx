export function NavBar({ activeTab, variant = 'blue' }: NavBarProps) {
  const navigate = useNavigate();

  const isPurple = variant === 'purple';
  const activeColor = isPurple ? '#6E30E8' : '#135BEC';
  const inactiveColor = isPurple ? '#64748B' : '#94A3B8';
  const bgColor = isPurple ? 'rgba(22,17,33,0.9)' : 'rgba(15,20,32,0.9)';
  const borderColor = isPurple ? 'rgba(255,255,255,0.05)' : '#1e293b';

  return (
    <div
      className="flex flex-col items-start w-full pt-[13px] pb-[32px] px-[16px] relative"
      style={{ background: bgColor, borderTop: `1px solid ${borderColor}`, backdropFilter: 'blur(12px)' }}
    >
      <div className="flex items-center justify-between w-full">
        {navItems.map(({ tab, label, path, d, vb, w, h }) => {
          const isActive = activeTab === tab;
          const color = isActive ? activeColor : inactiveColor;
          return (
            <button
              key={tab}
              onClick={() => navigate(path)}
              className="flex flex-col items-center gap-[4px] flex-1 cursor-pointer bg-transparent border-0"
            >
              <svg fill="none" height={h} viewBox={vb} width={w}>
                <path d={d} fill={color} />
              </svg>
              <span
                className="text-[10px]"
                style={{
                  color,
                  fontFamily: "'Manrope', sans-serif",
                  fontWeight: isActive ? 700 : 500,
                }}
              >
                {label}
              </span>
              {tab === 'home' && isActive && !isPurple && (
                <div className="absolute bottom-[28px] rounded-full" style={{ width: 4, height: 4, background: '#38BDF8' }} />
              )}
            </button>
          );
        })}
      </div>
      <div
        className="absolute bottom-[8px] left-1/2 -translate-x-1/2 rounded-full"
        style={{ width: 128, height: 5, background: isPurple ? 'rgba(255,255,255,0.2)' : '#1e293b' }}
      />
    </div>
  );
}